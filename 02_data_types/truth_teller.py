"""Notice how booleans guide decisions and reflections in code."""

# We can set up a simple scenario to check in with ourselves.
is_energy_high = False
has_deadline_today = True

# Booleans respond directly, encouraging clear decisions.
print("Energy high?", is_energy_high)
print("Deadline today?", has_deadline_today)

# Use them to guide gentle logic later in the journey.
should_rest = (not is_energy_high) and (not has_deadline_today)
print("Is rest the loving choice?", should_rest)

