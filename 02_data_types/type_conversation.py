"""Spend time with several data types and notice how they express themselves."""

# Integers feel like steady footfalls.
days_until_break = 4

# Floats are softer, capturing nuance.
tea_temperature = 72.5

# Strings bring narrative.
letter_to_self = "Keep trusting your learning rhythm."

# Booleans provide calm certainty.
is_rest_day_planned = True

print("Footsteps to rest:", days_until_break)
print("Temperature of comfort:", tea_temperature)
print("Letter to self:", letter_to_self)
print("Is today a rest day?", is_rest_day_planned)

