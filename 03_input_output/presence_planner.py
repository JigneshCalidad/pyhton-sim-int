"""Gather plans for the day and reflect them back as encouragement."""

activity = input("What is one meaningful activity you will focus on today? ")
duration = input("How many minutes do you want to devote to it? ")

# Even though input returns strings, we can still speak to the intention behind them.
print(f"Setting aside {duration} minutes for {activity} sounds like a nourishing choice.")
print("May your focus feel grounded and kind.")

