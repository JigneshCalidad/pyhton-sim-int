"""Define a function that walks through a grounding ritual."""

def brew_tea(blend: str) -> None:
    """Describe a mindful tea ritual for the given blend."""
    print(f"Preparing to steep {blend}.")
    print("Boil water patiently.")
    print("Steep the leaves while setting an intention.")
    print("Pour slowly and notice the aroma.")
    print("Sip with gratitude for this pause.")


brew_tea("chamomile")

