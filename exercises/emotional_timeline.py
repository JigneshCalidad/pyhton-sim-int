"""Chart an emotional timeline and notice emerging patterns."""

moods = [
    ("Morning", "quiet"),
    ("Afternoon", "energized"),
    ("Evening", "reflective"),
]

for time_of_day, mood in moods:
    print(f"{time_of_day}: feeling {mood}")

